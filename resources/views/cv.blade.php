                <h4 class="pb-2 text-[#9F6969]">Artist's Statement</h4>
                <p class="text-xs text-gray-400 leading-relaxed">
                    I am fascinated by the human form and it's ability to express emotions that communicate feelings in a verbal and non verbal way. <br>
                    Using the human figure as a motif, that spans across the design of the series, I re-interpret the traditional notion of portraiture and painting using drawn monochromatic figures made with ballpoint pen and ink in flat bold acrylic color backgrounds on large scale paper. I use paper as a reflection of life; it is precious, strong and yet very fragile.<br>
                    I employ a method of shading which I refer to as 'layering' and I use this 'layering' as a metaphor for the process of self actualization and coming into the consciousness of individual identity.<br>
                    Each piece is a visual expression of emotions and experiences. The colors are deliberately bright as a metaphor for "Happiness, peace and contentment" juxtaposed within a theme of Afro urban style and pop culture to tell a story of confidence, self awareness and determination in a world where cultural concepts and systems are transient.
                </p>
